class Defaultvalue{
    
	int intValue;
	float floatValue;
	long longValue;
	short shortValue;
	char charvalue;
	byte byteValue;
	
	String stringValue;
	


	
	public static void main(String args[]){
		
		Defaultvalue dvalue = new Defaultvalue();
		
		
		System.out.println("default value for int " +dvalue.intValue);
		
		System.out.println("default value for float" +dvalue.floatValue);
		
		System.out.println("default value for long" +dvalue.longValue);
		
		System.out.println("default value for short" +dvalue.shortValue);
		
		System.out.println("default value for  char" +dvalue.charvalue);
		System.out.println("default value for byte" +dvalue.byteValue);
		
		
		System.out.println("default value for string" +dvalue.stringValue);
		System.out.println("default value for array" +dvalue.arrayValue);
		
		
		
		
	}




}